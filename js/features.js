// Функція оновлення "Сторіз" на головному екрані
function updateDailyStory(day) {
    const storyEl = document.getElementById('story-text');
    if (!storyEl) return;

    let story = "";
    
    // Логіка Flo: Текст залежить від дня циклу
    if (day <= 5) {
        story = "Рівень прогестерону низький. Це час для відпочинку. Твій організм оновлюється. Уникай важких навантажень 🧘.";
    } else if (day <= 12) {
        story = "Естроген зростає. Ти відчуваєш приплив енергії та креативності. Гарний час для спорту та нових починань 🚀!";
    } else if (day <= 16) {
        story = "Період овуляції ✨. Шанси на вагітність найвищі. Шкіра виглядає сяючою. Можливе загострення емоцій.";
    } else if (day <= 28) {
        story = "Передменструальний період. Обмін речовин прискорюється, може зрости апетит. Будь ніжною до себе ❤️.";
    } else {
        story = "Затримка — це нормально при стресі або зміні клімату. Але якщо вона триває довго, варто зробити тест або звернутися до лікаря.";
    }
    
    // Замінюємо "Завантаження..." на реальний текст
    storyEl.innerText = story;
}
